function greet(name) {
    return "Hello, " + name + "!";
}

function evenCheck(numToCheck) {
    if (numToCheck % 2 === 0) {
        return numToCheck + " is even.";
    } else {
        return numToCheck + " is not even.";
    }
}
function squareNumber(numToSquare) {
    return numToSquare * numToSquare;
}
function farenToC(faren) {
    return (faren - 32) * 5 / 9;
    }
function milesTraveled(mph, hours){
    return "You travelled " + (mph * hours) + " miles"
}


